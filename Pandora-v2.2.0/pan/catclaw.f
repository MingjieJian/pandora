      subroutine CATCLAW
     $(XLM,VL1)
C
C     Rudolf Loeser, 1982 May 18
C---- Computes H- absorption crosssection, for CATFOOT.
C     Table from: Wishart, A.W. 1979, MNRAS, V187, P59.
C     Lambda in Angstroms, crosssection in 1.D-17/cm**2.
C     (This is version 2 of CATCLAW.)
C     !DASH
      save
C     !DASH
      real*8 SIG, VL1, XLAM, XLM
      integer NLAM, jummy
C     !DASH
      external LININT, HI, BYE
C
      dimension XLAM(67), SIG(67)
C
      data NLAM /67/
      data XLAM /
     $ 0.0000D0, .50000D3, 1.0000D3, 1.2500D3, 1.5000D3, 1.7500D3,
     $ 2.0000D3, 2.2500D3, 2.5000D3, 2.7500D3, 3.0000D3, 3.2500D3,
     $ 3.5000D3, 3.7500D3, 4.0000D3, 4.2500D3, 4.5000D3, 4.7500D3,
     $ 5.0000D3, 5.2500D3, 5.5000D3, 5.7500D3, 6.0000D3, 6.2500D3,
     $ 6.5000D3, 6.7500D3, 7.0000D3, 7.2500D3, 7.5000D3, 7.7500D3,
     $ 8.0000D3, 8.2500D3, 8.5000D3, 8.7500D3, 9.0000D3, 9.2500D3,
     $ 9.5000D3, 9.7500D3, 10.000D3, 10.250D3, 10.500D3, 10.750D3,
     $ 11.000D3, 11.250D3, 11.500D3, 11.750D3, 12.000D3, 12.250D3,
     $ 12.500D3, 12.750D3, 13.000D3, 13.250D3, 13.500D3, 13.750D3,
     $ 14.000D3, 14.250D3, 14.500D3, 14.750D3, 15.000D3, 15.250D3,
     $ 15.500D3, 15.750D3, 16.000D3, 16.100D3, 16.200D3, 16.300D3,
     $ 16.421D3/
      data SIG /
     $ 0.0000D0, .18000D0, .39000D0, .54310D0, .65120D0, .79180D0,
     $ .94530D0, 1.1080D0, 1.2750D0, 1.4460D0, 1.6190D0, 1.7920D0,
     $ 1.9650D0, 2.1350D0, 2.3020D0, 2.4650D0, 2.6240D0, 2.7770D0,
     $ 2.9230D0, 3.0620D0, 3.1940D0, 3.3170D0, 3.4320D0, 3.5370D0,
     $ 3.6320D0, 3.7170D0, 3.7910D0, 3.8540D0, 3.9070D0, 3.9480D0,
     $ 3.9770D0, 3.9950D0, 4.0010D0, 3.9950D0, 3.9770D0, 3.9480D0,
     $ 3.9060D0, 3.8530D0, 3.7890D0, 3.7130D0, 3.6250D0, 3.5280D0,
     $ 3.4190D0, 3.3010D0, 3.1720D0, 3.0340D0, 2.8870D0, 2.7330D0,
     $ 2.5710D0, 2.4020D0, 2.2260D0, 2.0460D0, 1.8620D0, 1.6740D0,
     $ 1.4850D0, 1.2950D0, 1.1070D0, .92110D0, .74070D0, .56770D0,
     $ .40520D0, .25750D0, .13020D0, .08697D0, .04974D0, .01989D0,
     $ 0.0000D0/
C
      call HI ('CATCLAW')
C     !BEG
      call LININT (XLAM,1,SIG,1,NLAM, XLM,VL1, 1,1,jummy)
C     !END
      call BYE ('CATCLAW')
C
      return
      end

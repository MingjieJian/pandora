      subroutine DUANE
     $(K,KD,ISO,EM)
C
C     Rudolf Loeser, 1993 Mar 19
C---- Computes M(K,K+dK,0) for FRESH.
C     ISO may equal 12 or 13.
C     !DASH
      save
C     !DASH
      real*8 TAB
      integer EM, I, ISO, K, KD
C     !DASH
      external HI, BYE
C
      dimension TAB(21,2,2)
C
      data (TAB(I,1,1),I=1,21) /
     $  1.0634D-01,  1.5015D-01,  1.8359D-01,  2.1163D-01,  2.3618D-01,
     $  2.5824D-01,  2.7840D-01,  2.9704D-01,  3.1444D-01,  3.3078D-01,
     $  3.4620D-01,  3.6083D-01,  3.7475D-01,  3.8802D-01,  4.0070D-01,
     $  4.1283D-01,  4.2444D-01,  4.3556D-01,  4.4620D-01,  4.5637D-01,
     $  4.6608D-01/
      data (TAB(I,2,1),I=1,21) /
     $ -6.6723D-03, -1.1695D-02, -1.6719D-02, -2.1796D-02, -2.6934D-02,
     $ -3.2126D-02, -3.7367D-02, -4.2656D-02, -4.7983D-02, -5.3349D-02,
     $ -5.8747D-02, -6.4180D-02, -6.9645D-02, -7.5146D-02, -8.0682D-02,
     $ -8.6258D-02, -9.1876D-02, -9.7545D-02, -1.0327D-01, -1.0905D-01,
     $ -1.1490D-01/
      data (TAB(I,1,2),I=1,21) /
     $  1.0515D-01,  1.4848D-01,  1.8156D-01,  2.0929D-01,  2.3358D-01,
     $  2.5541D-01,  2.7536D-01,  2.9381D-01,  3.1104D-01,  3.2721D-01,
     $  3.4249D-01,  3.5699D-01,  3.7078D-01,  3.8394D-01,  3.9651D-01,
     $  4.0855D-01,  4.2008D-01,  4.3113D-01,  4.4172D-01,  4.5186D-01,
     $  4.6155D-01/
      data (TAB(I,2,2),I=1,21) / 21*0.D0 /
C
      call HI ('DUANE')
C     !BEG
      EM = TAB((K+1),KD,(ISO-11))**2
C     !END
      call BYE ('DUANE')
C
      return
      end

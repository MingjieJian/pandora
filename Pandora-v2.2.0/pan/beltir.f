     $ 1.74D1, 1.90D1, 2.03D1, 2.23D1, 2.57D1, 2.61D1, 2.78D1, 2.89D1/
      data (U(I,6,2),I=1,16) /
     $ 2.91D0, 4.53D0, 6.06D0, 7.32D0, 9.17D0, 1.05D1, 1.14D1, 1.21D1,
     $ 1.31D1, 1.38D1, 1.44D1, 1.51D1, 1.56D1, 1.63D1, 1.68D1, 1.71D1/
      data (U(I,7,2),I=1,16) /
     $ 5.25D0, 7.26D0, 8.47D0, 9.27D0, 1.03D1, 1.08D1, 1.12D1, 1.14D1,
     $ 1.17D1, 1.18D1, 1.19D1, 1.19D1, 1.20D1, 1.19D1, 1.19D1, 1.19D1/
      data (U(I,4,3),I=1,16) /

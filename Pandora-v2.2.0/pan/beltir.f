     $ 1.33D-1, 1.45D-1, 1.53D-1, 1.58D-1, 1.65D-1, 1.70D-1, 1.77D-1,
     $ 1.82D-1, 1.85D-1/
      data (U(I,7,1),I=1,16) /
     $ 5.03D-2, 6.72D-2, 7.86D-2, 8.74D-2, 1.00D-1, 1.10D-1, 1.16D-1,
     $ 1.21D-1, 1.27D-1, 1.31D-1, 1.34D-1, 1.36D-1, 1.37D-1, 1.39D-1,
     $ 1.39D-1, 1.40D-1/
      data (U(I,3,2),I=1,16) /
     $ 2.35D1, 2.78D1, 3.09D1, 3.38D1, 4.01D1, 4.71D1, 5.45D1, 6.20D1,

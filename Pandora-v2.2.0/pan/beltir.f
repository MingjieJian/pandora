C
      data ZERO /0.D0/
      data KODE /1/
C
      call HI ('BELTIR')
C     !BEG
      UPSILON = ZERO
C
      IUOK = (IU.gt.1).and.(IU.lt.8)
      ILOK = (IL.gt.0).and.(IL.lt.IU)
      if(IUOK.and.ILOK) then
        JS = 0
        call CUBINT (T, 1, U(1,IU,IL), 1, 16, TE, UPSILON, KODE, IRET,
     $               JS, C)
      end if
C     !END
      call BYE ('BELTIR')
C
      return
      end

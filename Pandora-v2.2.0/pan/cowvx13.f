      EN = ERROR
      if(V.le.MAXV) then
        G = GBDHLM13C16O(1,V+1)
        B = GBDHLM13C16O(2,V+1)
        D = GBDHLM13C16O(3,V+1)*F1
        H = GBDHLM13C16O(4,V+1)*F2
